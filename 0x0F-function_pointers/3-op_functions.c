#include "3-calc.h"


/**
 *op_add - function to add two integer
 *@a: integer
 *@b: integer
 *Return: a+b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - function to sub b from a
 *@a: integer
 *@b: integer
 *Return: a-b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - function to multiply a and b
 *@a: integer
 *@b: integer
 *Return: a*b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - function to b divid a and b
 *@a: integer
 *@b: integer
 *Return: a/b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - function modulo a, b.
 *@a: integer
 *@b: integer
 *Return: a % b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
